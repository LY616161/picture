package com.so.picture.user.entry;


public class User {
    public int userId;
    public String userName;
    public int userMovie;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getUserMovie() {
        return userMovie;
    }

    public void setUserMovie(int userMovie) {
        this.userMovie = userMovie;
    }
}
